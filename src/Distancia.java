
public class Distancia {
	int vertice;
	double x;
	double y;
	
	public Distancia(int vertice, double x, double y) {
		super();
		this.vertice = vertice;
		this.x = x;
		this.y = y;
	}
	
	public int vertice() {
		return vertice;
	}
	public void setVertice(int vertice) {
		this.vertice = vertice;
	}
	public double x() {
		return x;
	}
	public void setX(double x) {
		this.x = x;
	}
	public double y() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}
	
	
	
}
